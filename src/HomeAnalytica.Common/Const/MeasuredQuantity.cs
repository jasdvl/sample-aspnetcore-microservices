namespace HomeAnalytica.Common.Const
{
    /// <summary>
    /// Represents measured quantities.
    /// </summary>
    public enum MeasuredQuantity
    {
        /// <summary>
        /// Represents an unknown or undefined measured quantity.
        /// </summary>
        Unknown = 0,

        /// <summary>
        /// Represents temperature as a measured quantity.
        /// </summary>
        Temperature = 1,

        /// <summary>
        /// Represents humidity as a measured quantity.
        /// </summary>
        Humidity = 2,

        /// <summary>
        /// Represents energy consumption as a measured quantity.
        /// </summary>
        EnergyConsumption = 3
    }
}
